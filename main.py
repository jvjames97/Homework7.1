mood = input("How are you?")
if mood == "happy":
    print("It is great to see you happy!")
elif mood == "nervous":
    print("Take a deep breath 3 times.")
elif mood == "sad":
    print("Could be worse...")
elif mood == "excited":
    print("Go for it!")
elif mood == "relaxed":
    print("Feeling good?")
else:
    print("I don't recognize this mood")